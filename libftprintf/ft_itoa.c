#include "libft.h"

void	ft_itoa(int n, char *nb)
{
	int		i;

	if (n == -2147483648)
		ft_strcpy(nb , "-2147483648");
	i = ft_ilen(n);
	if (n < 0 && n != -0 && n != -2147483648)
	{
		nb[0] = '-';
		n = -n;
	}
	if (n == 0)
		nb[0] = '0';
	while (n > 0)
	{
		i--;
		nb[i] = n % 10 + 48;
		n = n / 10;
	}
}
