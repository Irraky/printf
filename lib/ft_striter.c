#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	int		j;

	j = -1;
	if (s && *s && f)
	{
		i = ft_strlen(s);
		while (++j != i)
			f(&s[j]);
	}
}
