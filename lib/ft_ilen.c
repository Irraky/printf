#include "libft.h"

int		ft_ilen(int n)
{
	int		i;

	if (n == -2147483648)
		return (11);
	if (n == 0 || n == +0 || n == -0)
		return (1);
	i = (n < 0) ? 1 : 0;
	n = (n < 0) ? -n : n;
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
