#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;
	int		j;

	j = -1;
	if (s && *s && f)
	{
		i = ft_strlen(s);
		while (++j != i)
			f(j, &s[j]);
	}
}
